     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = response()->json($request->only(['name', 'phone', 'email', 'order_info']));
        $filename = $request->input('name') . "_" . $request->input('phone') . ".json";
        $pathname = '/saved_json_files/'.$filename;
        Storage::disk('local')->put($pathname, json_encode($data));
        return view('unloadingorder.index', ['pathname' => $pathname]);
    }

